function v = RA232488_L02_02_aux1(t)
    v = zeros(size(t));
    
    v(t < 0) = 0;
    v(t >= 0 & t < 8) = 10*t(t >= 0 & t < 8).^2 - 5*t(t >= 0 & t < 8);
    v(t >= 8 & t < 16) = 624 - 3*t(t >= 8 & t < 16);
    v(t >= 16 & t <= 26) = 36*t(t >= 16 & t <= 26) + 12*(t(t >= 16 & t <= 26) - 16).^2;
    v(t > 26) = 2136 * exp(-0.1 * (t(t > 26) - 26));

end
